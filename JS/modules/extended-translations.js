export const extendPageTranslations = (baseTranslations, pageName) => {
  const pageTranslations = extendedPageTranslations[pageName] || {};

  return Object.entries(pageTranslations).reduce((translations, [locale, copy]) => ({
    ...translations,
    [locale]: {
      ...baseTranslations.en,
      ...copy
    }
  }), { ...baseTranslations });
};

export const extendReasonTranslationMap = (baseTranslations) => (
  Object.fromEntries(Object.entries(baseTranslations).map(([reasonId, copy]) => [
    reasonId,
    Object.entries(extendedReasonTranslations).reduce((translations, [locale, reasons]) => ({
      ...translations,
      [locale]: reasons[reasonId] || copy.en
    }), { ...copy })
  ]))
);
